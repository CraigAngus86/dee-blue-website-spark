// Additional helper functions
export const getMatchesByMonth = (matches) => {
  const matchesByMonth = {};
  
  matches.forEach(match => {
    const date = new Date(match.date);
    const monthYear = date.toLocaleString('en-US', { month: 'long', year: 'numeric' });
    
    if (!matchesByMonth[monthYear]) {
      matchesByMonth[monthYear] = [];
    }
    
    matchesByMonth[monthYear].push(match);
  });
  
  return matchesByMonth;
};

export const getAvailableMonths = () => {
  // Return all months from both fixtures and results
  const allMatches = [...fixturesData.matches, ...resultsData.results];
  const months = new Set();
  
  allMatches.forEach(match => {
    const date = new Date(match.date);
    const monthYear = date.toLocaleString('en-US', { month: 'long', year: 'numeric' });
    months.add(monthYear);
  });
  
  return Array.from(months) as string[];
};

export const getAvailableCompetitions = () => {
  // Return all unique competitions
  const allMatches = [...fixturesData.matches, ...resultsData.results];
  const competitions = new Set();
  
  allMatches.forEach(match => {
    competitions.add(match.competition);
  });
  
  return Array.from(competitions) as string[];
};

